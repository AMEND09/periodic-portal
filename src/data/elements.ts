// Function to get element positions for the periodic table grid
export const getElementPosition = (element: Element) => {
  // Handle special cases for lanthanides and actinides
  if (element.category === 'lanthanide') {
    return { row: 8, col: element.number - 56 };
  }
  if (element.category === 'actinide') {
    return { row: 9, col: element.number - 88 };
  }
  
  // For normal elements
  return { row: element.period, col: element.group || 0 };
};

// Function to check if a position is a placeholder in the table
export const isPlaceholder = (row: number, col: number) => {
  // Placeholders for lanthanides and actinides in the main table
  if (row === 6 && col === 3) return { type: 'lanthanide-placeholder', text: '57-71' };
  if (row === 7 && col === 3) return { type: 'actinide-placeholder', text: '89-103' };
  
  // Empty cells
  if ((row === 1 && col > 1 && col < 18) ||
      (row === 2 && col > 2 && col < 13) ||
      (row === 3 && col > 2 && col < 13)) {
    return { type: 'empty' };
  }
  
  return null;
};
